package dominio;

public class Avance {
	String notas;
	String fecha;

	Avance () {

	}

	public void setNotas (String notas) {
		this.notas = notas;
	}

	public String getNotas () {
		return notas;
	}

	public void setFecha (String fecha) {
		this.fecha = fecha;
	}

	public String getFecha () {
		return fecha;
	}
}
